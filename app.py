from flask import Flask, render_template, request, send_file
import pandas as pd
from sklearn.linear_model import LogisticRegression
from fpdf import FPDF
import io

app = Flask(__name__)

# --- Model Training logic ---
try:
    df = pd.read_csv("diabetes.csv")

    # Column names se extra spaces hatana (Aapka error yahi se tha)
    df.columns = df.columns.str.strip()

    # Target column check karna
    target_col = "Outcome"
    if target_col not in df.columns:
        # Agar capital O nahi hai, toh small o check karein
        target_col = "outcome" if "outcome" in df.columns else df.columns[-1]

    X = df.drop(target_col, axis=1)
    y = df[target_col]

    model = LogisticRegression(max_iter=1000)
    model.fit(X.values, y)
    print(f"✅ Model trained successfully using column: {target_col}")

except Exception as e:
    print(f"❌ Error loading CSV or training model: {e}")


@app.route("/")
def index():
    return render_template("index.html", result=None)


@app.route("/predict", methods=["POST"])
def predict():
    if request.method == "POST":
        try:
            # Form se data nikalna
            inputs = [
                float(request.form["preg"]),
                float(request.form["glu"]),
                float(request.form["bp"]),
                float(request.form["skin"]),
                float(request.form["ins"]),
                float(request.form["bmi"]),
                float(request.form["dpf"]),
                float(request.form["age"]),
            ]

            # Prediction
            prediction = model.predict([inputs])[0]
            result = (
                "Diabetic (High Risk)" if prediction == 1 else "Non-Diabetic (Healthy)"
            )

            return render_template("index.html", result=result, inputs=inputs)
        except Exception as e:
            return f"Prediction Error: {str(e)}"


@app.route("/download_report", methods=["POST"])
def download_report():
    try:
        res = request.form.get("result", "Not Available")
        preg = request.form.get("Pregnancies", "0")
        glu = request.form.get("Glucose", "0")
        age = request.form.get("Age", "21")

        # PDF creation
        pdf = FPDF()
        pdf.add_page()
        pdf.set_font("Arial", "B", 16)
        pdf.cell(200, 10, txt="Diabetes Prediction Report", ln=True, align="C")
        pdf.ln(10)

        pdf.set_font("Arial", size=12)
        pdf.cell(200, 10, txt=f"Patient Age: {age}", ln=True)
        pdf.cell(200, 10, txt=f"Glucose Level: {glu}", ln=True)
        pdf.cell(200, 10, txt=f"Pregnancies: {preg}", ln=True)
        pdf.ln(5)

        pdf.set_font("Arial", "B", 14)
        pdf.cell(200, 10, txt=f"FINAL RESULT: {res}", ln=True)
        pdf.ln(20)
        pdf.set_font("Arial", "I", 10)
        pdf.cell(
            200, 10, txt="Generated by Kishan Kumar's AI Predictor", ln=True, align="R"
        )

        # Memory mein PDF generate karna
        pdf_out = pdf.output(dest="S").encode("latin-1")
        output = io.BytesIO(pdf_out)

        return send_file(
            output,
            as_attachment=True,
            download_name="Diabetes_Report.pdf",
            mimetype="application/pdf",
        )
    except Exception as e:
        return f"PDF Error: {str(e)}"


if __name__ == "__main__":
    app.run(debug=True)
